package com.danidemi.jlubricant.screenscraping;

/**
 * Generic checked exception related to screen scraping.
 */
public class ScreenScrapingException extends Exception {

	private static final long serialVersionUID = -4672267046039840815L;

	public ScreenScrapingException() {
		super();
	}

	public ScreenScrapingException(String message, Throwable cause) {
		super(message, cause);
	}

	public ScreenScrapingException(String message) {
		super(message);
	}

	public ScreenScrapingException(Throwable cause) {
		super(cause);
	}

	
	
}
